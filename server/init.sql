CREATE DATABASE IF NOT EXISTS semicapstone;
USE semicapstone;

CREATE TABLE IF NOT EXISTS sensor_readings (
    id INT AUTO_INCREMENT PRIMARY KEY,

    device_id VARCHAR(50) NOT NULL DEFAULT 'chamber_1',
    sequence INT NOT NULL DEFAULT 0,

    state VARCHAR(20) NOT NULL DEFAULT 'NORMAL',

    co2 INT NOT NULL DEFAULT 0,
    temperature DOUBLE NOT NULL DEFAULT 0.0,
    humidity DOUBLE NOT NULL DEFAULT 0.0,
    pm1_0 INT NOT NULL DEFAULT 0,
    pm2_5 INT NOT NULL DEFAULT 0,
    pm10 INT NOT NULL DEFAULT 0,
    voc INT NOT NULL DEFAULT 0,

    fan_speed INT NOT NULL DEFAULT 40,
    fan_voltage DOUBLE NOT NULL DEFAULT 0.0,
    fan_current_mA DOUBLE NOT NULL DEFAULT 0.0,
    fan_power_W DOUBLE NOT NULL DEFAULT 0.0,

    measured_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    received_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);


